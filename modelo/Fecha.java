package modelo;
public class Fecha
{
    //---Atributos---
    private int dia;
    private int mes;
    private int anio;

    //---Metodos---
    public int getDia() {
        return dia;
    }
    public void setDia(int dia) {
        this.dia = dia;
    }
    public int getMes() {
        return mes;
    }
    public void setMes(int mes) {
        this.mes = mes;
    }
    public int getAnio() {
        return anio;
    }
    public void setAnio(int anio) {
        this.anio = anio;
    }


    


}